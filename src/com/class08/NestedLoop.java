package com.class08;

public class NestedLoop {

	public static void main(String[] args) {
		
		for (int i = 1; i < 3; i++) {   //outer loop
			System.out.println("I am outer loop");
			  for (int j = 1; j <= 3; j++) {//inner loop
				  System.out.println("I am inner loop");
				  
	    }
	 		  
	  }
		
	}

}
